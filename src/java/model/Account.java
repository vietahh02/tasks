package model;

/**
 *
 * @author Admin
 */
public class Account {

    private String userID, user, pass;

    public Account() {
    }

    public Account(String userID, String user, String pass) {
        this.userID = userID;
        this.user = user;
        this.pass = pass;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    

}
